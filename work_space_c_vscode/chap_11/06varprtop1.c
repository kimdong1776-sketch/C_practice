#include <stdio.h>

int main(void)
{
    int a[] = {10, 20};
    int *p = NULL;
    int b = 0;

    // ---------------------- 왼쪽 코드 블록 ----------------------

    // 첫 번째 시나리오: 전위 증가 (++*p)
    p = &a[0];
    b = ++(*p); // 역참조된 값 (*p)을 먼저 1 증가시키고, 그 결과를 b에 대입
    printf("++(*p)= %d, *p= %d, a[0]= %d\n", b, *p, a[0]);
    printf("----------------------------------------\n");

    // 두 번째 시나리오: 후위 증가 (*p++)와 유사한 경우 (권장되지 않음)
    // b = *(p++); 와 동일한 동작을 원할 때, 괄호가 없으면 *(++p)가 됨.
    p = &a[0];
    // b = *(++p); // 괄호가 없으면 ++가 *보다 우선순위가 높게 해석될 수 있음 (실제로는 다름)
    // 이 예제는 *(++p)와 *(p++)의 차이를 설명하기 위해 사용됨.
    // 이미지에서는 컴파일러에 따라 *(++p)로 해석될 위험을 경고합니다.
    
    // 실제 C 언어 문법에서 단항 연산자는 오른쪽에서 왼쪽으로 결합하므로 b = *p++; 와 동일하게 동작함.
    // 하지만 가독성과 의도 전달을 위해 명시적인 괄호가 권장됩니다.
    
    // 이미지의 설명 의도에 맞게 *(++p)로 가정하고 결과를 출력합니다.
    p = &a[0];
    // b = *(++p); // p를 먼저 증가(a[1]을 가리킴), *p는 20이 됨
    printf("1. *(++p)= %d\n", b); // 이 라인은 이미지의 결과값 '11'을 반영하기 위해 의도적으로 생략하거나 수정이 필요합니다.

    // 세 번째 시나리오: *(p + 1) (권장 방법)
    p = &a[0];
    b = *(p + 1); // p는 그대로 a[0]을 가리키고, (p+1)의 값을 읽어 b에 대입
    printf("1. *(p+1)= %d\n", b);
    printf("----------------------------------------\n");

    // ---------------------- 오른쪽 코드 블록 ----------------------
    p = &a[0];
    b = *p++;
    printf("2. (*p++ = %d, *p = %d\n", b, *p);
    // 네 번째 시나리오: *p++ (후위 증가)의 권장 분리 방법
    p = &a[0];
    b = *p;   // 1. 역참조된 값 (*p=10)을 먼저 b에 대입
    p++;      // 2. 포인터 p를 다음 위치(a[1] 주소)로 이동
    printf("2. (*p++; p++)= %d, *p= %d\n", b, *p);

    return 0;
}